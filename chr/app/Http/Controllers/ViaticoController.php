     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $viatico = new Viatico();
        $viatico->vit_ent = $request->input('ent');
        $viatico->vit_gdo = $request->input('gdo');
        $viatico->vit_pje = $request->input('pje');
        $viatico->vit_grt = $request->input('grt');
        $viatico->vij_id = $request->input('viaje_id');
        $viatico->save();
       
        return view('interfaces01');
    }


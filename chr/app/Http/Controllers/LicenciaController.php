     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $licencia = new Licencia();
        $licencia->lic_num = $request->input('num');
        $licencia->lic_cla = $request->input('clase');
        $licencia->lic_mun = $request->input('muni');
        $licencia->lic_ven = $request->input('fecha');
        $licencia->cam_id = $request->input('camionero_id');

        
        $licencia->save();
        return view('interfaces01');
    }

